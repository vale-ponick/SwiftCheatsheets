# Задачи на тернарный оператор

## Задача 1
Напиши функцию, которая принимает два числа и возвращает `"positive"`, `"negative"` или `"mixed"`.

## Задача 2
Напиши функцию, которая принимает три числа и возвращает `"ascending"`, `"descending"` или `"mixed"`.

## Задача 3
Дан массив строк. Оставь слова длиннее 5 символов, приведи к верхнему регистру, отсортируй по алфавиту. Используй цепочку `filter`, `map`, `sorted`.
